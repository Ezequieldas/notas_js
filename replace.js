// Método replace - Solo reemplaza el primer término que encuentra
const string = "Javascript es maravilloso, con Javascript puedo crear el futuro de la web";

const replaceString = string.replace('Javascript','Python')
console.log(replaceString);

// Método replaceAll - Incorporado en ES12 - Reemplaza todos los términos que encuentre por el nuevo

const string2 = "Javascript es maravilloso, con Javascript puedo crear el futuro de la web";

const replaceString2 = string.replaceAll('Javascript','Python')
console.log(replaceString2);


